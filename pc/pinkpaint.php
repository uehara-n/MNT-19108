	<h3>ピンクペイント運動への想い<img src="http://www.miwa-p.jp/wp-content/themes/corpo/page_image/pinkpaint/pinkpaint_logo.png" alt="ピンクペイント運動"  class="logo"></h3>
	<div class="pinkpaint_box3">
	<p class="mb20"><span class="pinkpaint_tit">ピンクペイント運動のきっかけ</span><br />
	なぜ、関市の塗装会社が、「ピンクペイント運動」を始めたのか、不思議に思われることでしょう。<br />
	しかし、私にとって乳がんは、とても身近で重要な課題です。
	</p>
	<p class="mb20"><span class="pinkpaint_tit">乳がんが身近な課題</span><br />
	実は20数年前、私の母親は乳がんと診断され手術を行いました。<br />
	おかげさまで術後の経過がよく、20数年経った現在でも元気に暮らしています。<br />
	しかし、妻のお母さんは、乳がんで亡くなられています。<br />
	妻はすでにお母さんが亡くなられた歳を超えましたが、決して油断をしてはならないと思っています。
	</p>
	<p class="mb20"><span class="pinkpaint_tit">三輪塗装で働く女性たちへ</span><br />
	さらに三輪塗装では、40歳前後の女性社員が4名働いてくれています。<br />
	40歳代と言えば、乳がんの要注意世代。乳がん検診を受診してもらいましたが、気を抜いてはならないと考えています。
	</p>
	<p class="mb20"><span class="pinkpaint_tit">自分たちの仕事で出来ること</span><br />
	乳がんは早期発見をして、適切な治療をすれば良好な経過が期待できます。<br />
	しかし、乳がんへの関心が低いせいか、検診の受診率が低いというのが現状です。<br />
	そこで当社では、乳がんの早期発見の大切さを知っていただくため、「ピンクペイント運動」を進めています。
	</p>
	<p class="mb20"><img src="http://www.miwa-p.jp/wp-content/themes/corpo/page_image/pinkpaint/pinkpaint_p.png" alt="三輪塗装代表" class="img-responsive f_r">
	<span class="pinkpaint_tit">運動を通じての願い</span><br />
	この運動を通じて、乳がんのリスクから一人でも多くの女性を守ることができたら、私としても嬉しく、この運動を推進した意味があると思っています。<br />
	ぜひ、活動に対するご理解と、ご賛同頂ければ幸いです。
	</p>
