      　これらの結果から、「病気の治療と仕事の両立」に関する<br>男女の意識の差が明らかになりました。<br>
        <br>乳がん罹患時に、女性は半数以上が「仕事を続けたい」<br>「配偶者にも仕事に影響のない範囲で配偶者にサポートしてほしい」と<br>
        回答しており、「病気の治療と仕事の両立」を望んでいる割合が<br>多いことが分かりました。<br>
        <br>一方男性は、配偶者が乳がん罹患時に「仕事を続けてほしい」と<br>回答した割合は 2 割にとどまり、「最優先でサポートしたい」<br>
        「仕事をセーブしてサポートしたい」と回答した割合は 6 割にのぼり、<br>「配偶者には病気の治療を優先してほしい、それをサポートしたい」と<br>望んでいる割合が多いことが分かりました。
